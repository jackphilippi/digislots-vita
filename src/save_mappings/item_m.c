const int item_m_SIZE = sizeof(itemMap) / sizeof(ItemMapping);

char* getItemName(unsigned int searchId) {
    const char* itemName = "Unknown";
    for (int i = 0; i < item_m_SIZE; i++) {
        if (itemMap[i].value == searchId) {
            itemName = itemMap[i].name;
            break;
        }
    }
    return (char*)itemName;
}